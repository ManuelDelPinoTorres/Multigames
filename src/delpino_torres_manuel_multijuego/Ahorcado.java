    public static boolean validarNumeros(String datos) {
        return datos.matches("[a-zA-Z]*");
    }

    private void jMenu3MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenu3MousePressed
        // TODO add your handling code here:
        if (this.palabraGanadora.equalsIgnoreCase("")) {
            this.palabraGanadora = JOptionPane.showInputDialog("Introduzca una palabra de 5 letras.");

            if (this.palabraGanadora == null) {
                this.palabraGanadora = "";
            }

            while (this.palabraGanadora.length() != 5 || this.palabraGanadora.equalsIgnoreCase("") || !Ahorcado.validarNumeros(palabraGanadora)) {
                if (this.palabraGanadora.equalsIgnoreCase("")) {
                     JOptionPane.showMessageDialog(this, "Debe introducir una palabra para comenzar a jugar.", "Error", JOptionPane.ERROR_MESSAGE);
                    this.palabraGanadora = "";
                } else if (!Ahorcado.validarNumeros(this.palabraGanadora)) {
                    JOptionPane.showMessageDialog(this, "La palabra no puede contener simbolos ni numeros", "Error", JOptionPane.ERROR_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "La palabra introducida debe tener 5 letras", "Error", JOptionPane.ERROR_MESSAGE);
                }
                this.palabraGanadora = JOptionPane.showInputDialog("Introduzca una palabra de 5 letras.");

                if (this.palabraGanadora == null) {
                    this.palabraGanadora = "";
                }
            }
            this.esJuegoesmpezado = true;
        } else {
            JOptionPane.showMessageDialog(this, "Ya hay una palabra introducida debe reiniciar.");
        }
    }//GEN-LAST:event_jMenu3MousePressed

