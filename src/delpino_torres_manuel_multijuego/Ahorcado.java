    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (!this.palabraGanadora.equalsIgnoreCase("")) {
            this.esJuegoesmpezado = true;
            if (this.intentos < this.ahorcado.length) {
                boolean acierto = false;
                String letra = JOptionPane.showInputDialog(this, "Introduzca una letra");

                while (letra.length() > 1 || !Ahorcado.validarNumeros(letra)) {
                    if (letra.length() > 1) {
                        JOptionPane.showMessageDialog(this, "No puede introducir mas de una letra", "Error", JOptionPane.ERROR_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(this, "No puede introducir numeros o simbolos", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                    letra = JOptionPane.showInputDialog(this, "Introduzca solo una letra");
                }
                for (int i = 0; i < this.palabraGanadora.length(); i++) {
                    String letraGanadora = this.palabraGanadora.substring(i, i + 1);

                    if (letraGanadora.equalsIgnoreCase(letra)) {
                        resultado[i].setText(letra);
                        acierto = true;
                        if (this.continuarJugando() == -1) {
                            JOptionPane.showMessageDialog(this, "Enhorabuena " + this.nombreGanador() + " has ganado");
                            this.reiniciarJuego();
                            this.esJuegoesmpezado = false;
                        }
                    }
                }
                if (!acierto) {
                    jTextArea1.setText(jTextArea1.getText() + " " + this.intentos + " - " + letra + "\n");
                    this.intentos++;
                    if (this.intentos < this.ahorcado.length) {
                        this.ahorcado[this.intentos - 1].setVisible(true);
                        JOptionPane.showMessageDialog(this, "Has fallado te quedan " + (MAX_INTENTOS - this.intentos) + " intentos.");
                    } else {
                        this.ahorcado[this.intentos - 1].setVisible(true);
                        JOptionPane.showMessageDialog(this, "Has perdido!!,la palabra era " + this.palabraGanadora + " es turno de " + this.nombreCambioJugador());
                        this.reiniciarJuego();
                    }

                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Para comenzar a jugar introduzca una palabra");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private String nombreCambioJugador() {
        if (this.turno == 1) {
            this.turno = 2;
            return jLabel9.getText();
        } else {
            this.turno = 1;
            return jLabel8.getText();
        }

    }

    private String nombreGanador() {
        if (this.turno == 1) {
            this.turno = 2;
            this.victoriasJugador1++;
            jLabel10.setText("Numero de victorias: " + this.victoriasJugador1);
            jLabel16.setText(this.establecerTurno());
            return jLabel8.getText();
        } else {
            this.turno = 1;
            this.victoriasJugador2++;
            jLabel11.setText("Numero de victorias: " + this.victoriasJugador2);
            jLabel16.setText(this.establecerTurno());
            return jLabel9.getText();
        }

    }

