    private int continuarJugando() {
        int pos = -1;
        int i = 0;
        while (i < this.resultado.length && this.resultado[i].getText() != "-") {
            i++;
        }

        if (i < this.resultado.length) {
            pos = i;
        }

        return pos;
    }

    private void reiniciarJuego() {
        this.reiniciarDibujo();
        this.reiniciarCasillas();
        this.intentos = 0;
        this.palabraGanadora = "";
        this.jTextArea1.setText("");
    }

    private void reiniciarCasillas() {
        for (int i = 0; i < this.resultado.length; i++) {
            this.resultado[i].setText("-");
        }

    }

    private void reiniciarDibujo() {
        for (int i = 0; i < this.ahorcado.length; i++) {
            this.ahorcado[i].setVisible(false);
        }
    }

